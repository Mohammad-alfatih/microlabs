class TriangleLevel4 {
    public static void main(String[] args) {
        
        int j = 5;
        while(j>0) {
            
            int i = j;
            while(i>0) {
                System.out.print("*");
                i--;
            }
            
            System.out.print("\n");
            j--;
        }
    }
}
