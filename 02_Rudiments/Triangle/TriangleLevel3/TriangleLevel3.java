class TriangleLevel3 {
    public static void main(String[] args) {
        // Same exact code as TriangleLevel2 App. Because this code still meets the conditons.
        for(int j=5;j>0;j--) {
            for(int i=j;i>0;i--) {
                System.out.print("*");
            }
            System.out.print("\n");
        }
        
    }
}
